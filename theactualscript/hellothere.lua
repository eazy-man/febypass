coroutine.wrap(function()
local script = LocalScript3
script.Parent.MouseButton1Click:Connect(function()
	-- Config --
	local minimumsize = Vector3.new(0.7,0.7,0.7) -- Minimum size for a part to get divided, higher numbers = less detailed and bigger/less bricks
	local surface_between_splitted_parts = 'SmoothNoOutlines' -- the surfacetype between splitted parts
	local fragmentable = workspace -- all fragmentable objects should be stored in here. dont change unless you only want certain parts to fragment
	local fillup = 1000 -- it constantly generates new parts until it reaches this number (hacky way to prevent lagspikes if there is a large explosion),change it to 0 if you donÂ´t want it to generate (useless) parts.
	local maximumstorage = 2000 -- it will recycle parts if the number of parts in the storage doesnt exceed this number
	local storage_position = Vector3.new(0,0,5000000) -- place them somewhere off the map
	local stored_partsize = Vector3.new(1,1,1) -- make them small
	local parts_created_per_frame = 5 -- number of parts being created per frame to fill up the storage
	local fuse = 2.5 -- grenade fuse

	-- Main --
	local lp = game:GetService("Players").LocalPlayer
	local ch = lp.Character
	local list = {}
	local brickcount = 0
	local storage = {}

	function fragmentate(cframe,size,color,explosion_position,explosion_blastradius,backsurface,bottomsurface,frontsurface,leftsurface,rightsurface,topsurface,transparency,reflectance,material)
		local xi = size.X >= minimumsize.X*(1+explosion_blastradius/16) and 2 or 1 -- to reduce the lagg in large explosions we increase minimumsize based on the explosionradius...
		local yi = size.Y >= minimumsize.Y*(1+explosion_blastradius/16) and 2 or 1
		local zi = size.Z >= minimumsize.Z*(1+explosion_blastradius/16) and 2 or 1
		if not material then material = "Plastic" end
		if xi == 1 and yi == 1 and zi == 1 or (cframe.p-explosion_position).magnitude > size.magnitude/2 + explosion_blastradius then -- don't fragmentate parts, that are too small to fragmentate or too far away from the explosion
			if xi == 1 and yi == 1 and zi == 1 then return end -- optional
			if #storage > 0 then
				local p = storage[1]
				p.BrickColor = color
				p.Size = size
				p.BackSurface = backsurface
				p.BottomSurface = bottomsurface
				p.FrontSurface = frontsurface
				p.LeftSurface = leftsurface
				p.RightSurface = rightsurface
				p.TopSurface = topsurface
				p.Transparency = transparency
				p.Material = material
				p.CFrame = cframe
				p.Reflectance = reflectance
				table.remove(storage,1)
			else
				local p = Instance.new("Part",fragmentable)
				p.BrickColor = color
				p.FormFactor = "Custom"
				p.Size = size
				p.BackSurface = backsurface
				p.BottomSurface = bottomsurface
				p.FrontSurface = frontsurface
				p.LeftSurface = leftsurface
				p.RightSurface = rightsurface
				p.TopSurface = topsurface
				p.Transparency = transparency
				p.Material = material
				if p.Transparency>0.285 then -- make glass shatter lol
					p.Anchored = false
				else
					p.Anchored=true
				end
				p.CFrame = cframe
				p.Reflectance = reflectance
			end
			return -- stop the function
		end
		local mody = math.random(-125,125)/1000 -- some randomization
		for y = 1,yi do
			if math.random()> 0.5 then
				local modx = math.random(-125,125)/1000
				for x = 1,xi do
					local modz = math.random(-125,125)/1000
					for z = 1,zi do -- offset = x/xi-0.75+modx)
						fragmentate(cframe*CFrame.new(size.X*(xi==1 and 0 or x/xi-0.75+modx),size.Y*(yi==1 and 0 or y/yi-0.75+mody),size.Z*(zi==1 and 0 or z/zi-0.75+modz)), -- maths
							Vector3.new(xi == 2 and size.X*(1-2*math.abs(x/xi-0.75+modx)) or size.X,yi == 2 and size.Y*(1-2*math.abs(y/yi-0.75+mody)) or size.Y,
								zi == 2 and size.Z*(1-2*math.abs(z/zi-0.75+modz)) or size.Z or nil),color,explosion_position,explosion_blastradius,
							z~=zi and surface_between_splitted_parts or backsurface,y==2 and surface_between_splitted_parts or bottomsurface,
							z==2 and surface_between_splitted_parts or frontsurface,x==2 and surface_between_splitted_parts or leftsurface,x~=xi and surface_between_splitted_parts or rightsurface,
							y~=yi and surface_between_splitted_parts or topsurface,transparency,reflectance,material) 
					end

				end
			else
				local modz = math.random(-125,125)/1000
				for z = 1,zi do
					local modx = math.random(-125,125)/1000
					for x = 1,xi do
						fragmentate(cframe*CFrame.new(size.X*(xi==1 and 0 or x/xi-0.75+modx),size.Y*(yi==1 and 0 or y/yi-0.75+mody),size.Z*(zi==1 and 0 or z/zi-0.75+modz)),
							Vector3.new(xi == 2 and size.X*(1-2*math.abs(x/xi-0.75+modx)) or size.X,yi == 2 and size.Y*(1-2*math.abs(y/yi-0.75+mody)) or size.Y,
								zi == 2 and size.Z*(1-2*math.abs(z/zi-0.75+modz)) or size.Z),color,explosion_position,explosion_blastradius,
							z~=zi and surface_between_splitted_parts or backsurface,y==2 and surface_between_splitted_parts or bottomsurface,
							z==2 and surface_between_splitted_parts or frontsurface,x==2 and surface_between_splitted_parts or leftsurface,x~=xi and surface_between_splitted_parts or rightsurface,
							y~=yi and surface_between_splitted_parts or topsurface,transparency,reflectance,material)
					end
				end
			end
		end				
	end

	function start_fragmentation(position,radius)
		local search = Region3.new(position-Vector3.new(radius,radius,radius)*1.1,position+Vector3.new(radius,radius,radius)*1.1)
		repeat
			local finish = false
			local parts = workspace:FindPartsInRegion3WithIgnoreList(search,list,100) -- maximum number of parts that FindPartsInRegion3 can find is 100, so we have to do this to find them all
			for i = 1,#parts do
				table.insert(list,1,parts[i])
			end
			finish = true
		until #parts < 100 and finish
		print(#list)
		local t = tick()
		for i = 1,#list do
			local p = list[i]
			if p:IsDescendantOf(fragmentable) and p:IsDescendantOf(ch)==false then
				fragmentate(p.CFrame,p.Size,p.BrickColor,position,radius,p.BackSurface,p.BottomSurface,p.FrontSurface,p.LeftSurface,p.RightSurface,p.TopSurface,p.Transparency,p.Reflectance,p.Material)
				if #storage < maximumstorage and p.Shape == "Block" then -- recycle them
					p.Anchored = false
					p.FormFactor = "Custom"
					p.Size = stored_partsize
					p.Position = storage_position
					table.insert(storage,1,p)
				else -- storage is full
					p:Destroy()
				end
			end
		end	
		list = {}
	end

	function AngleOfReach(distance, altitude, velocity)
		local hasGravity, gravity = pcall(function() return workspace.Gravity end) -- support gravity for revs with gravity (2016M and onwards)
		if not hasGravity then gravity = 196.2 end -- else resort to default
		local theta = math.atan((velocity^2 + math.sqrt(velocity^4 -gravity*(gravity*distance^2 + 2*altitude*velocity^2)))/(gravity*distance))
		if theta ~= theta then
			theta = math.pi/4
		end
		return(theta)
	end

	local mouse;

	local grenade = Instance.new("Tool", game:GetService("Players").LocalPlayer.Backpack)
	grenade.Name = "Grenade"

	local handle = Instance.new("Part", grenade)
	handle.Name = "Handle"
	handle.Size = Vector3.new(0.8, 0.92, 0.8)
	handle.BrickColor = BrickColor.new(141)

	local mesh = Instance.new("SpecialMesh", handle)
	mesh.Scale = Vector3.new(1.3, 1.5, 1.3)
	mesh.MeshId = "rbxassetid://166887416"

	grenade.Equipped:Connect(function(m)
		mouse = m
	end)

	grenade.Activated:Connect(function()
		local position = mouse.Hit.p
		local handlePos = Vector3.new(handle.Position.X, 0, handle.Position.Z) -- remove Y from the equation, it's not needed
		local mousePos = Vector3.new(position.X, 0, position.Z) -- ditto
		local distance = (handlePos - mousePos).magnitude -- distance between mouse and handle
		local altitude = position.Y - handle.Position.Y -- get altitude
		local angle = AngleOfReach(distance, altitude, 100) -- calculate the angle

		local newGrenade = handle:Clone()
		newGrenade.Name = "Grenade"
		newGrenade.Parent = workspace
		newGrenade.CanCollide = true

		local position = mouse.Hit.p
		newGrenade.Velocity = (CFrame.new(newGrenade.Position, Vector3.new(position.X, newGrenade.Position.Y, position.Z)) * CFrame.Angles(angle, 0, 0)).lookVector * 100 -- throwing calc, maybe a tad long lol

		wait(fuse) -- fuse	

		local boom = Instance.new("Explosion")
		boom.BlastPressure = 0 -- non destructive, since the fragmentation is the destruction
		boom.BlastRadius = 7.5
		boom.Hit:Connect(function(hit)
			if hit.Parent:FindFirstChild("Humanoid") then
				hit.Parent:breakJoints()
			end
		end)
		boom.Position = newGrenade.Position
		boom.Parent = workspace
		start_fragmentation(newGrenade.Position, 7.5)
		newGrenade:Destroy()
	end)
	-- Check Cashed (CheckkCashedd) | 11/15/2024
end)
end)()

